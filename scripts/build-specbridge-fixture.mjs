// Build the SpecBridge fixture workspace used by the specbridge-* skill
// evals. Copies the sidecar-managed example project from the SpecBridge
// repo, then installs + enables the reference analyzer extension through the
// real CLI so extension/registry tools have real state to read.
//
// Env:
//   SPECBRIDGE_REPO  path to the SpecBridge checkout (default: ../specbridge)
import { cpSync, existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { execFileSync } from "node:child_process";
import path from "node:path";
import { fileURLToPath } from "node:url";

const templateRoot = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const specbridgeRepo = process.env.SPECBRIDGE_REPO ?? path.resolve(templateRoot, "..", "specbridge");
const cli = path.join(specbridgeRepo, "packages", "cli", "dist", "index.js");
const source = path.join(specbridgeRepo, "examples", "requirements-first-project");
const fixture = path.join(templateRoot, "fixtures", "specbridge-workspace");

if (!existsSync(cli)) {
  console.error(`build-specbridge-fixture: built CLI not found at ${cli} — run pnpm build in SpecBridge first.`);
  process.exit(1);
}
if (!existsSync(source)) {
  console.error(`build-specbridge-fixture: example project not found at ${source}`);
  process.exit(1);
}

rmSync(fixture, { recursive: true, force: true });
mkdirSync(path.dirname(fixture), { recursive: true });
cpSync(source, fixture, { recursive: true });

const run = (...args) =>
  execFileSync(process.execPath, [cli, ...args], { cwd: fixture, encoding: "utf8" });

// Package each reference extension (deterministic zip + checksums), then
// install the archive — the same flow a real user follows.
const packAndInstall = (id) => {
  const dir = path.join(specbridgeRepo, "examples", "extensions", id);
  const packed = JSON.parse(run("extension", "package", dir, "--json"));
  run("extension", "install", packed.data.archivePath);
  rmSync(packed.data.archivePath, { force: true });
};

// Enabled analyzer + deliberately disabled verifier exercise both paths.
packAndInstall("example-analyzer");
const show = JSON.parse(run("extension", "show", "example-analyzer", "--json"));
run("extension", "enable", "example-analyzer", "--accept-permissions", show.data.permissionHash);
packAndInstall("example-verifier");

// A second spec WITH a tasks document (the sidecar-managed example is still
// in DESIGN_DRAFT), so task-oriented skills have real open/done tasks.
cpSync(
  path.join(specbridgeRepo, "examples", "existing-kiro-project", ".kiro", "specs", "user-authentication"),
  path.join(fixture, ".kiro", "specs", "user-authentication"),
  { recursive: true },
);

// Snapshots: pretty-printed captures of real CLI output, committed with the
// fixture so registry/runner/template facts have a citable file:line. The
// tools re-run the CLI live and fail loudly if a snapshot drifts.
const snapshotsDir = path.join(fixture, "snapshots");
mkdirSync(snapshotsDir, { recursive: true });
const snapshot = (name, ...args) => {
  const parsed = JSON.parse(run(...args, "--json"));
  const file = path.join(snapshotsDir, `${name}.json`);
  writeFileSync(file, `${JSON.stringify(parsed.data, null, 2)}\n`, "utf8");
};
snapshot("runner-list", "runner", "list");
snapshot("template-list", "template", "list");
snapshot("verify-rules", "verify", "rules");

// WORKSPACE.md: one factual line per entity, generated from the same CLI
// output (never hand-written), so coarse workspace facts have a citable
// line whose text carries the fact's own terms. Tools verify freshness.
const specs = JSON.parse(run("spec", "list", "--json")).data.specs;
const doctor = JSON.parse(run("doctor", "--json")).data;
const extensions = JSON.parse(run("extension", "list", "--json")).data.extensions;
const specLines = specs.map((s) => {
  const status = JSON.parse(run("spec", "status", s.name, "--json")).data;
  const stages = (status.stages ?? [])
    .map((stage) => `${stage.stage} ${stage.effective}`)
    .join(", ");
  return (
    `- spec ${s.name}: type ${s.type}, mode ${s.workflowMode}, ` +
    `status ${status.effectiveStatus ?? status.status}, managed ${s.managed}` +
    (stages ? `, stages: ${stages}` : "") +
    `, tasks ${s.taskProgress.completed}/${s.taskProgress.total} done`
  );
});
const workspaceDoc = [
  "# Workspace facts (generated by build-specbridge-fixture.mjs from real CLI output — do not edit)",
  "",
  `- workspace: healthy ${doctor.healthy}, specs ${specs.length}, kiro layout detected`,
  ...specLines,
  ...extensions.map(
    (e) => `- extension ${e.id}: version ${e.version}, kind ${e.kind}, ${e.enabled ? "enabled" : "disabled"}`,
  ),
  "",
].join("\n");
writeFileSync(path.join(fixture, "WORKSPACE.md"), workspaceDoc, "utf8");

console.log("fixture ready:", fixture);
console.log(JSON.parse(run("extension", "list", "--json")).data.extensions.map((e) => `${e.id}:${e.enabled ? "enabled" : "disabled"}`).join(", "));
